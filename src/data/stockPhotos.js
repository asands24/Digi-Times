// Helper function to get photos by category
export const getPhotosByCategory = (category) => {
  const categoryKey = category.toLowerCase()
  return stockPhotos[categoryKey] || stockPhotos.general
}

// Helper function to get random photos
export const getRandomPhotos = (count = 3, category = null) => {
  let photoPool = []

  if (category) {
    photoPool = getPhotosByCategory(category)
  } else {
    // Get photos from all categories
    photoPool = Object.values(stockPhotos).flat()
  }

  const shuffled = [...photoPool].sort(() => 0.5 - Math.random())
  return shuffled.slice(0, count)
}

// Helper function to get all photos
export const getAllPhotos = () => {
  return Object.values(stockPhotos).flat()
}

// Helper function to search photos by tag
export const searchPhotosByTag = (tag) => {
  const allPhotos = getAllPhotos()
  return allPhotos.filter(photo =>
    photo.tags.some(t => t.toLowerCase().includes(tag.toLowerCase()))
  )
}

